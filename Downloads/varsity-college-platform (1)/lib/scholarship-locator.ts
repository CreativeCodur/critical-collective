export interface ScholarshipFilters {
  location: string[]
  type: string[]
  amount: string[]
  deadline: string[]
  programs: string[]
  demographics: string[]
  eligibility: string[]
}

export function filterScholarships(
  scholarships: ScholarshipData[],
  filters: Partial<ScholarshipFilters>,
  searchQuery?: string,
): ScholarshipData[] {
  return scholarships.filter((scholarship) => {
    // Search query filter
    if (searchQuery) {
      const query = searchQuery.toLowerCase()
      const matchesSearch =
        scholarship.name.toLowerCase().includes(query) ||
        scholarship.description.toLowerCase().includes(query) ||
        scholarship.provider.toLowerCase().includes(query)
      if (!matchesSearch) return false
    }

    // Location filter
    if (filters.location && filters.location.length > 0) {
      const hasLocationMatch = filters.location.some(
        (loc) => scholarship.states.includes(loc) || scholarship.states.includes("ALL"),
      )
      if (!hasLocationMatch) return false
    }

    // Type filter
    if (filters.type && filters.type.length > 0) {
      if (!filters.type.includes(scholarship.type)) return false
    }

    // Programs filter
    if (filters.programs && filters.programs.length > 0) {
      const hasProgramMatch = filters.programs.some(
        (prog) => scholarship.programs.includes(prog) || scholarship.programs.includes("ALL"),
      )
      if (!hasProgramMatch) return false
    }

    // Demographics filter
    if (filters.demographics && filters.demographics.length > 0) {
      const hasDemographicMatch = filters.demographics.some(
        (demo) => scholarship.demographics.includes(demo) || scholarship.demographics.includes("ALL"),
      )
      if (!hasDemographicMatch) return false
    }

    return true
  })
}

export function suggestScholarshipsForCollege(college: CollegeData): ScholarshipData[] {
  const suggestions: ScholarshipData[] = []

  // Add all scholarships and filter by relevance
  const allScholarships = [...scholarshipDatabase]

  const relevantScholarships = allScholarships.filter((scholarship) => {
    let relevanceScore = 0

    // State match (high priority)
    if (scholarship.states.includes(college.state) || scholarship.states.includes("ALL")) {
      relevanceScore += 10
    }

    // College name match
    if (scholarship.colleges.some((collegeName) => college.name.includes(collegeName))) {
      relevanceScore += 15
    }

    // Program match based on college characteristics
    if (college.engineeringPercent > 25 && scholarship.programs.includes("Engineering")) {
      relevanceScore += 8
    }
    if (college.businessPercent > 30 && scholarship.programs.includes("Business")) {
      relevanceScore += 8
    }

    // Institution type match
    if (college.ownership === 1 && scholarship.eligibility.some((req) => req.includes("Public"))) {
      relevanceScore += 5
    }
    if (college.ownership === 2 && scholarship.eligibility.some((req) => req.includes("Private"))) {
      relevanceScore += 5
    }

    // Need-based for high tuition schools
    if (college.tuitionOutState > 40000 && scholarship.type === "need-based") {
      relevanceScore += 6
    }

    // Merit-based for selective schools
    if (college.admissionRate && college.admissionRate < 20 && scholarship.type === "merit") {
      relevanceScore += 6
    }

    return relevanceScore >= 5 // Only include scholarships with decent relevance
  })

  // Sort by relevance and return top suggestions
  return relevantScholarships.slice(0, 15)
}

export function getScholarshipStats(scholarships: ScholarshipData[]) {
  const totalAmount = scholarships.reduce((sum, scholarship) => {
    const amount = scholarship.amount.match(/\$[\d,]+/)
    if (amount) {
      const numericAmount = Number.parseInt(amount[0].replace(/[$,]/g, ""))
      return sum + numericAmount
    }
    return sum
  }, 0)

  const typeDistribution = scholarships.reduce(
    (acc, scholarship) => {
      acc[scholarship.type] = (acc[scholarship.type] || 0) + 1
      return acc
    },
    {} as Record<string, number>,
  )

  return {
    totalScholarships: scholarships.length,
    totalAmount,
    typeDistribution,
  }
}
