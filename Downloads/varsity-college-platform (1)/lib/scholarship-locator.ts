// Initialize with base scholarships and load CSV data
let scholarshipDatabase: ScholarshipData[] = [...baseScholarshipDatabase]
let csvScholarshipsLoaded = false
let seedsOfFortuneLoaded = false

// Function to load and merge all scholarship sources
export async function initializeScholarshipDatabase(): Promise<void> {
  if (csvScholarshipsLoaded && seedsOfFortuneLoaded) return

  try {
    console.log("Loading scholarships from all sources...")

    // Update existing scholarship dates first
    const updatedBaseScholarships = updateExistingScholarshipDates(baseScholarshipDatabase)
    scholarshipDatabase = [...updatedBaseScholarships]

    // Load original CSV scholarships if not already loaded
    if (!csvScholarshipsLoaded) {
      const csvScholarships = await loadScholarshipsFromCSV()
      const newCsvScholarships = csvScholarships.filter(
        (csvScholarship) => !isDuplicateScholarship(csvScholarship, scholarshipDatabase),
      )
      scholarshipDatabase = [...scholarshipDatabase, ...newCsvScholarships]
      csvScholarshipsLoaded = true
      console.log(`Added ${newCsvScholarships.length} scholarships from original CSV files`)
    }

    // Load Seeds of Fortune scholarships if not already loaded
    if (!seedsOfFortuneLoaded) {
      const seedsScholarships = await loadSeedsOfFortuneScholarships()
      const newSeedsScholarships = seedsScholarships.filter(
        (seedsScholarship) => !isDuplicateScholarship(seedsScholarship, scholarshipDatabase),
      )
      scholarshipDatabase = [...scholarshipDatabase, ...newSeedsScholarships]
      seedsOfFortuneLoaded = true
      console.log(`Added ${newSeedsScholarships.length} scholarships from Seeds of Fortune CSV files`)
    }

    console.log(`Total scholarships in database: ${scholarshipDatabase.length}`)
  } catch (error) {
    console.error("Error initializing scholarship database:", error)
  }
}

